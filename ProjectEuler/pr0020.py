"""
n! means n × (n − 1) × ... × 3 × 2 × 1

For example, 10! = 10 × 9 × ... × 3 × 2 × 1 = 3628800,
and the sum of the digits in the number 10! is 3 + 6 + 2 + 8 + 8 + 0 + 0 = 27.

Find the sum of the digits in the number 100!
"""

NUMBER = 100

prod = 1
for a in range(1,NUMBER+1):
    prod *= a

sum = 0
for b in str(prod):
    sum += int(b)

print(sum)